    running_specs = example.metadata[:file_path]

    # Detect the source engine if it is rswag specs
    if running_specs.include?("example_group_helpers.rb")
      running_specs = example.metadata[:example_group][:absolute_file_path]
    end

    # Get the actual running spec file (not the helper)
    engine_match = running_specs.match(%r{bx/([^/]+)/spec}) if running_specs

    if engine_match
      engine_name = engine_match[1]
      engine_spec_path = Rails.root.join("bx", engine_name, "spec")
      engine_support_path = engine_spec_path.join("support")

      # Resetting requires and reloading FactoryBot setup (for each file)
      @last_running_spec ||= nil
      if @last_running_spec != running_specs
        @last_running_spec = running_specs
        # Reset require list to remove previously loaded helpers and factory_bot files
        $LOADED_FEATURES.reject! do |file|
          file.match?(%r{/bx/[^/]+/spec/}) && (  # Ensure file is inside bx/.../spec/
            file.end_with?("rails_helper.rb") ||
            file.end_with?("support/unit/factory_bot.rb")
          ) ||
          file.include?("/factories")  # Match any factories-related file
        end

        if defined?(FactoryBot)
          FactoryBot.factories.clear 
          FactoryBot.reload
        end

        # Reload the current engine’s helpers
        rails_helper_path = engine_spec_path.join("rails_helper.rb")
        factory_bot_path = engine_support_path.join("unit/factory_bot.rb")
        
        load rails_helper_path if File.exist?(rails_helper_path)
        # Reload FactoryBot setup and dependencies for this engine
