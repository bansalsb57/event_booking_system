require 'rails_helper'
include Warden::Test::Helpers

RSpec.describe Admin::BroadcastersController, type: :controller do
  render_views

  let(:admin_user) do 
    AdminUser.create!(
      name: "admin",
      email: "adminadmin@example.com",
      password: "Password1!!",
      password_confirmation: "Password1!!",
      user_type: "admin",
      activated: true
    )
  end

  let(:valid_attributes) { 
    {
      user_name: "Test Broadcaster",
      email: "broadcaster#{SecureRandom.hex(4)}@example.com",
      server_hostname: "localhost",
      server_port: 8000,
      server_password: "password123",
      server_mount: "/test",
      server_bitrate: 128
    }
  }

  let(:invalid_attributes) do
    {
      user_name: "",
      email: "",
      server_hostname: "",
      server_port: "",
      server_password: "",
      server_mount: "",
      server_bitrate: ""
    }
  end

  before(:each) do
    sign_in admin_user
  end

  before do
    allow(Broadcasters).to receive(:page_registered?).and_return(false)
    allow(Broadcasters).to receive(:is_being_loaded_from_app?).and_return(false)
  end

  it "does not call unload_activeadmin_resource when page is not registered" do
    expect(Broadcasters).not_to receive(:unload_activeadmin_resource)
    load Rails.root.join("bx/bx_block_admin/app/admin/broadcasters.rb")
  end

  context "when the page is registered and being loaded from app" do
    before do
      allow(Broadcasters).to receive(:page_registered?).with("Broadcaster").and_return(true)
      allow(Broadcasters).to receive(:is_being_loaded_from_app?).and_return(true)
    end

    it "calls unload_activeadmin_resource" do
      expect(Broadcasters).to receive(:unload_activeadmin_resource).with("Broadcaster")
      load Rails.root.join("bx/bx_block_admin/app/admin/broadcasters.rb")
    end
  end

  describe "GET /admin/broadcasters/new" do
    it "renders the new broadcaster form with server details section" do
      get :new
      
      expect(response).to be_successful
      expect(response.body).to include("Broadcaster Details")
      expect(response.body).to include("User Name")
      expect(response.body).to include("Email")
  
      # Only check for server details if admin is signed in
      expect(response.body).to include("Server hostname")
      expect(response.body).to include("Server port")
      expect(response.body).to include("Server password")
      expect(response.body).to include("Server mount")
      expect(response.body).to include("Server bitrate")
    end
  end

  describe 'POST #create' do
    context 'with valid attributes' do
      it 'creates a new Broadcaster and redirects' do
        expect {
          post :create, params: { account: valid_attributes }
        }.to change(AccountBlock::Account, :count).by(1)

        expect(response).to redirect_to(admin_broadcaster_path(AccountBlock::Account.last))
        expect(flash[:notice]).to eq("Broadcaster account created successfully. A password reset link has been sent to the email.")
      end

      it 'sets the correct user_type and activated values' do
        post :create, params: { account: valid_attributes }
        broadcaster = AccountBlock::Account.last

        expect(broadcaster.user_type).to eq("Broadcaster")
        expect(broadcaster.activated).to be_falsey
      end
    end

    context 'with invalid attributes' do
      it 'does not create a new Broadcaster and renders the new form' do
        expect {
          post :create, params: { account: invalid_attributes }
        }.not_to change(AccountBlock::Account, :count)

        expect(response).to render_template(:new)
        expect(response.body).to include("error")
      end
    end

    it 'permits the expected parameters' do
      expect(controller).to receive(:permitted_params).and_call_original
      post :create, params: { account: valid_attributes }
    end
    
  end
end

